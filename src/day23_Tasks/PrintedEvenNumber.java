package day23_Tasks;

public class PrintedEvenNumber {
    public static void main(String[] args) {
        evenNumber();
    }

    public static void evenNumber(){
        for (int i = 0; i <100 ; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " ");
            }
        }
            System.out.println();
        }
    }

